import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const collections = ['articles', 'contributions', 'projects', 'works'];
const locales = ['en', 'pt-BR'];
const sources = {};
const profiles = {};

for (const collection of collections) {
  for (const locale of locales) {
    const directory = path.join(root, 'src', 'content', collection, locale);
    for (const fileName of fs.readdirSync(directory).filter((name) => name.endsWith('.md'))) {
      const key = `${collection}/${locale}/${fileName.replace(/\.md$/, '')}`;
      sources[key] = fs.readFileSync(path.join(directory, fileName), 'utf8');
    }
  }
}

for (const locale of locales) {
  profiles[locale] = fs.readFileSync(path.join(root, 'src', 'content', 'profile', `${locale}.md`), 'utf8');
}

const output = `// Generated by scripts/build-content-data.mjs. Do not edit manually.\n\nexport const markdownSources = ${JSON.stringify(sources, null, 2)} as const;\n\nexport const profileSources = ${JSON.stringify(profiles, null, 2)} as const;\n`;
const outputPath = path.join(root, 'src', 'generated', 'content-data.ts');
fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, output);
